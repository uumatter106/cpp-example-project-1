
// Revision Example Topic: Printing On Screen

#include <iostream>

// Open `std` namespace.
using namespace std;

// Forward declarations of revision parts.
void part_a();
void part_b();
void part_c();

int main() {
    // Call all parts of program.
    part_a();
    part_b();
    part_c();
}

// Revision Part A
void part_a() {
    cout << "1 2 3 4" << endl;
}

// Revision Part B
void part_b() {
    cout << "1 " << "2 " << "3 " << "4" << endl;
}

// Revision Part C
void part_c() {
    cout << "1 ";
    cout << "2 ";
    cout << "3 ";
    cout << "4 ";
    cout << endl;
}
